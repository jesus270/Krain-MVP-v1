// Process natural language query into search terms
export const processSearchQuery = (query: string): string[] => {
  return query
    .toLowerCase()
    .replace(/[.,\/#!$%\^&\*;:{}=\-_`~()]/g, "")
    .split(/\s+/)
    .filter((word) => word.length > 1 && !stopWords.has(word));
};
